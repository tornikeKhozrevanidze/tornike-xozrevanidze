 # 2) დაწერეთ and და or ლოგიკური ოპერატორების ყველა შესაძლო გზა ამ დავალებაში გამოიყენეთ შედარების ოპერატორები 

#and
print(5<6 and 5>3) # True
print(4<3 and 4>5) # False
print(10>9 and 10>11) # False
print(23>24 and 24>23) # False


#or
print(6>=11 or 6<=11) # True
print(12>11 or 11>12) # True
print(22>23 or 23>24) # False
print(24>23 or 25>24) # True
